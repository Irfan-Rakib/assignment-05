// Added call to history
function addCallHistory(name, number) {
  const historyContainer = document.getElementById("history-card");

  const historyCard = document.createElement("div");
  historyCard.className =
    "history-card flex justify-between items-center rounded-lg mb-3 shadow-sm p-3 bg-white";

  const now = new Date();
  const timeString = now.toLocaleTimeString([], {
    hour: "2-digit",
    minute: "2-digit",
    second: "2-digit",
  });

  historyCard.innerHTML = `
    <div>
      <h4 class="font-bold">${name}</h4>
      <p>${number}</p>
    </div>
    <div class="date">${timeString}</div>
  `;

  historyContainer.prepend(historyCard);
}

//  All hotlines
const hotlines = [
  { id: 1, name: "National Emergency Number", number: "999" },
  { id: 2, name: "Police Helpline Number", number: "999" },
  { id: 3, name: "Fire Service Number", number: "102" },
  { id: 4, name: "Ambulance Service", number: "1994-999999" },
  { id: 5, name: "Women & Child Helpline", number: "109" },
  { id: 6, name: "Anti-Corruption Helpline", number: "106" },
  { id: 7, name: "Electricity Helpline", number: "16216" },
  { id: 8, name: "Brac Helpline", number: "16445" },
  { id: 9, name: "Bangladesh Railway Helpline", number: "163" },
];

// Attached events for each hotline
hotlines.forEach((line) => {
  const callBtn = document.getElementById(`call-${line.id}`);
  const copyBtn = document.getElementById(`copy-${line.id}`);
  const heartBtn = document.getElementById(`heart-${line.id}`);

  // Call button
  if (callBtn) {
    callBtn.addEventListener("click", () => {
      let coin = Number(document.getElementById("coin").innerText);

      if (coin >= 20) {
        document.getElementById("coin").innerText = coin - 20;
        addCallHistory(line.name, line.number);
        smoothAlert(`ðŸ“ž Calling ${line.name}: ${line.number}`);
      } else {
        smoothAlert("âš ï¸ You haven't enough coins!");
      }
    });
  }

  // Copy button
  if (copyBtn) {
    copyBtn.addEventListener("click", () => {
      navigator.clipboard.writeText(line.number).then(() => {
        let copyCount = Number(document.getElementById("copy").innerText);
        document.getElementById("copy").innerText = copyCount + 1;
        smoothAlert(`âœ… Copied: ${line.number}`);
      });
    });
  }

  // Heart button
  if (heartBtn) {
    heartBtn.addEventListener("click", () => {
      let heart = Number(document.getElementById("heart").innerText);
      document.getElementById("heart").innerText = heart + 1;
      smoothAlert("â¤ï¸ Congrats! You got 1 heart");
    });
  }
});

// Clear history
document.getElementById("clear-btn").addEventListener("click", () => {
  document.getElementById("history-card").innerHTML = "";
  smoothAlert("ðŸ—‘ï¸ History cleared!");
});

//  alert
function smoothAlert(message) {
  const alertBox = document.createElement("div");
  alertBox.className =
    "fixed top-1/8 right-1/4 bg-gray-800 text-white px-5 py-3 rounded-lg ";
  alertBox.textContent = message;

  document.body.appendChild(alertBox);

  setTimeout(() => {
    alertBox.style.opacity = "0";
    setTimeout(() => alertBox.remove(), 500);
  }, 2000);
}

